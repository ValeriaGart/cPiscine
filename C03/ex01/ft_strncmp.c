int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	j;

	j = 0;
	while (j < n && s1[j] != '\0' && s2[j] != '\0' && s1[j] == s2[j])
	{
		j++;
	}
	if (n == j)
		return (0);
	return (s1[j] - s2[j]);
}
